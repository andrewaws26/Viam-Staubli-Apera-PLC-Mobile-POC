// ── Config ────────────────────────────────────────────────────────────

const PROD_URL = "https://viam-staubli-apera-plc-mobile-poc.vercel.app";
const SCREENSHOT_DIR = path.join(__dirname, "..", "qa-screenshots");
const REPORT_PATH = path.join(__dirname, "..", "qa-report.md");

// Use production URL if QA_PROD=1, otherwise localhost
const BASE = process.env.QA_PROD === "1" ? PROD_URL : "http://localhost:3000";

// Collect results for report generation
const results: {
  name: string;
  status: "pass" | "fail" | "warn";
  detail: string;
  duration: number;
}[] = [];

function record(name: string, status: "pass" | "fail" | "warn", detail: string, duration: number) {
  results.push({ name, status, detail, duration });
}

// ── Setup ─────────────────────────────────────────────────────────────

test.beforeAll(() => {
  if (!fs.existsSync(SCREENSHOT_DIR)) {
    fs.mkdirSync(SCREENSHOT_DIR, { recursive: true });
  }
});

test.afterAll(() => {
  // Generate markdown report
  const now = new Date().toISOString();
  const passed = results.filter((r) => r.status === "pass").length;
  const failed = results.filter((r) => r.status === "fail").length;
  const warned = results.filter((r) => r.status === "warn").length;

  let report = `# IronSight QA Report\n\n`;
  report += `**Date:** ${now}\n`;
  report += `**Target:** ${BASE}\n`;
  report += `**Results:** ${passed} passed, ${failed} failed, ${warned} warnings\n\n`;

  if (failed > 0) {
    report += `## Failures\n\n`;
    for (const r of results.filter((r) => r.status === "fail")) {
      report += `- **${r.name}** (${r.duration}ms): ${r.detail}\n`;
    }
    report += `\n`;
  }

  if (warned > 0) {
    report += `## Warnings\n\n`;
    for (const r of results.filter((r) => r.status === "warn")) {
      report += `- **${r.name}** (${r.duration}ms): ${r.detail}\n`;
    }
    report += `\n`;
  }

  report += `## All Results\n\n`;
  report += `| Test | Status | Duration | Detail |\n`;
  report += `|------|--------|----------|--------|\n`;
  for (const r of results) {
    const icon = r.status === "pass" ? "PASS" : r.status === "fail" ? "FAIL" : "WARN";
    report += `| ${r.name} | ${icon} | ${r.duration}ms | ${r.detail.substring(0, 80)} |\n`;
  }

  report += `\n---\nGenerated by IronSight Overnight QA\n`;

  fs.writeFileSync(REPORT_PATH, report);
  console.log(`\n[QA] Report written to ${REPORT_PATH}`);
  console.log(`[QA] ${passed} passed, ${failed} failed, ${warned} warnings`);
});

async function screenshotPage(page: Page, name: string) {
  await page.screenshot({
    path: path.join(SCREENSHOT_DIR, `${name}.png`),
    fullPage: true,
  });
}

