// ── API Endpoint Health Checks ────────────────────────────────────────

test.describe("API Health Checks", () => {
  const API_ENDPOINTS = [
    { name: "Sensor Readings", path: "/api/sensor-readings?component=plc-monitor", method: "GET" },
    { name: "Truck Readings", path: "/api/truck-readings?component=truck-engine", method: "GET" },
    { name: "Fleet Status", path: "/api/fleet/status", method: "GET" },
    { name: "Fleet Trucks", path: "/api/fleet/trucks", method: "GET" },
    { name: "Work Orders", path: "/api/work-orders", method: "GET" },
    { name: "Team Members", path: "/api/team-members", method: "GET" },
    { name: "Shift Report", path: "/api/shift-report?hours=8", method: "GET" },
    { name: "DTC History", path: "/api/dtc-history?hours=24", method: "GET" },
    { name: "Maintenance", path: "/api/maintenance", method: "GET" },
  ];

  for (const ep of API_ENDPOINTS) {
    test(`${ep.name} responds`, async ({ request }) => {
      const start = Date.now();
      try {
        const res = await request.get(ep.path, { timeout: 15000 });
        const elapsed = Date.now() - start;

        if (res.status() === 401) {
          // Auth required — expected when not logged in, still counts as "alive"
          record(`API: ${ep.name}`, "warn", `401 Unauthorized (auth required) — ${elapsed}ms`, elapsed);
        } else if (res.ok()) {
          const body = await res.text();
          const isJson = body.startsWith("[") || body.startsWith("{");
          if (elapsed > 5000) {
            record(`API: ${ep.name}`, "warn", `Slow response: ${elapsed}ms`, elapsed);
          } else {
            record(`API: ${ep.name}`, "pass", `${res.status()} — ${isJson ? "valid JSON" : "text"} — ${elapsed}ms`, elapsed);
          }
        } else {
          record(`API: ${ep.name}`, "fail", `${res.status()} ${res.statusText()}`, elapsed);
        }
      } catch (e) {
        record(`API: ${ep.name}`, "fail", `Timeout or error: ${String(e)}`, Date.now() - start);
      }
    });
  }
});

